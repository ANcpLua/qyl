namespace Qyl.Agents.Generator.Generation;

using Models;

internal static class OutputGenerator
{
    public static FileWithName GenerateOutput(ServerModel server)
    {
        var sb = new IndentedStringBuilder();
        sb.Append(GeneratedCodeHelpers.AutoGeneratedHeader("Qyl.Agents.Generator"));
        sb.AppendLine(GeneratedCodeHelpers.NullableEnable);
        sb.AppendLine();

        if (!string.IsNullOrEmpty(server.Namespace))
        {
            sb.AppendLine($"namespace {server.Namespace};");
            sb.AppendLine();
        }

        var blocks = 0;
        var declArray = server.DeclarationChain.ToArray();
        for (var i = 0; i < declArray.Length; i++)
        {
            var decl = declArray[i];
            var isTarget = i == declArray.Length - 1;
            var baseType = isTarget ? " : global::Qyl.Agents.IMcpServer" : "";
            sb.AppendLine($"{decl.Modifiers} {decl.Keyword} {decl.Name}{decl.TypeParameters}{baseType}");
            foreach (var clause in decl.ConstraintClauses) sb.AppendLine(clause);
            sb.BeginBlock();
            blocks++;
        }

        OTelEmitter.Emit(sb, server);
        sb.AppendLine();
        SchemaEmitter.Emit(sb, server);
        DispatchEmitter.Emit(sb, server);
        ResourceEmitter.Emit(sb, server);
        PromptEmitter.Emit(sb, server);
        MetadataEmitter.Emit(sb, server);
        sb.AppendLine();
        SkillEmitter.Emit(sb, server);
        sb.AppendLine();
        LlmsTxtEmitter.Emit(sb, server);

        for (var i = 0; i < blocks; i++) sb.EndBlock();

        var hintName = string.IsNullOrEmpty(server.Namespace)
            ? $"{server.ClassName}.McpServer.g.cs"
            : $"{server.Namespace}.{server.ClassName}.McpServer.g.cs";

        return new FileWithName(hintName, sb.ToString());
    }
}
