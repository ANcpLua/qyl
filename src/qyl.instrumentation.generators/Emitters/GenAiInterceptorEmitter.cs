using Microsoft.CodeAnalysis.CSharp;
using Qyl.Instrumentation.Generators.Models;

namespace Qyl.Instrumentation.Generators.Emitters;

/// <summary>
///     Emits interceptor source code for GenAI SDK method invocations.
/// </summary>
/// <remarks>
///     Uses <see cref="ProviderRegistry" /> as the Single Source of Truth for provider definitions.
/// </remarks>
internal static class GenAiInterceptorEmitter
{
    private static readonly HashSet<string> TokenUsageOperations = new(StringComparer.Ordinal)
    {
        "chat",
        "embeddings",
        "invoke_agent",
        "text_completion",
        "generate_content"
    };

    private static readonly HashSet<string> InputOnlyOperations = new(StringComparer.Ordinal)
    {
        "embeddings", "rerank", "transcription"
    };

    public static string Emit(ImmutableArray<GenAiCallSite> invocations)
    {
        if (invocations.IsEmpty)
            return string.Empty;

        var sb = new IndentedStringBuilder();

        sb.AppendLineNoIndent("// <auto-generated/>");
        sb.AppendLineNoIndent("#pragma warning disable");
        sb.AppendLine();
        sb.AppendLine("using System.Diagnostics;");
        sb.AppendLine("using Qyl.Instrumentation.Instrumentation;");
        sb.AppendLine("using Qyl.Instrumentation.Instrumentation.GenAi;");
        sb.AppendLine("using qyl.contracts.Attributes;");
        sb.AppendLine();

        EmitterHelpers.AppendInterceptsLocationAttribute(sb);

        using (sb.BeginNamespace("Qyl.Instrumentation.Generators"))
        using (sb.BeginClass("file static", "GenAiInterceptors"))
        {
            var index = 0;
            foreach (var invocation in invocations.OrderBy(static i => i.SortKey, StringComparer.Ordinal))
            {
                if (invocation.IsStreaming)
                    AppendStreamingInterceptor(sb, invocation, index);
                else
                    AppendSingleInterceptor(sb, invocation, index);
                index++;
            }
        }

        return sb.ToString();
    }

    private static void AppendSingleInterceptor(IndentedStringBuilder sb, GenAiCallSite invocation, int index)
    {
        var displayLocation = invocation.Location.GetDisplayLocation();
        var interceptAttribute = invocation.Location.GetInterceptsLocationAttributeSyntax();
        var methodName = $"Intercept_GenAi_{index}";
        var returnType = invocation.ReturnTypeName;
        var originalMethod = invocation.MethodName;

        var argNames = GetPositionalNames(invocation.ParameterTypes.Length);
        var parameters =
            EmitterHelpers.BuildParameterList(invocation.ContainingTypeName, invocation.ParameterTypes, argNames);
        var arguments = EmitterHelpers.BuildArgumentList(argNames);

        var modelArg = invocation.Model is not null ? $"\"{invocation.Model}\"" : "null";
        var providerConst = GetProviderConstant(invocation.Provider);
        var operationConst = GetOperationConstant(invocation.Operation);
        var usageExtractor = GetUsageExtractor(invocation.Provider, invocation.Operation);

        sb.AppendLine($"// Intercepted call at {displayLocation}");
        sb.AppendLine(interceptAttribute);

        var asyncPrefix = invocation.IsAsync ? "async " : "";
        var awaitPrefix = invocation.IsAsync ? "await " : "";

        sb.AppendLine($"public static {asyncPrefix}{returnType} {methodName}({parameters})");
        using (sb.BeginBlock())
        {
            var executeMethod = invocation.IsAsync ? "ExecuteAsync" : "Execute";
            sb.AppendLine($"return {awaitPrefix}GenAiInstrumentation.{executeMethod}(");
            sb.AppendLine($"    {providerConst},");
            sb.AppendLine($"    {operationConst},");
            sb.AppendLine($"    {modelArg},");
            sb.AppendLine(usageExtractor is not null
                ? $"    {asyncPrefix}() => {awaitPrefix}@this.{originalMethod}({arguments}), {usageExtractor});"
                : $"    {asyncPrefix}() => {awaitPrefix}@this.{originalMethod}({arguments}));");
        }

        sb.AppendLine();
    }

    private static void AppendStreamingInterceptor(IndentedStringBuilder sb, GenAiCallSite invocation, int index)
    {
        var displayLocation = invocation.Location.GetDisplayLocation();
        var interceptAttribute = invocation.Location.GetInterceptsLocationAttributeSyntax();
        var methodName = $"Intercept_GenAi_{index}";
        var returnType = invocation.ReturnTypeName;
        var originalMethod = invocation.MethodName;

        var argNames = GetPositionalNames(invocation.ParameterTypes.Length);
        var parameters =
            EmitterHelpers.BuildParameterList(invocation.ContainingTypeName, invocation.ParameterTypes, argNames);
        var arguments = EmitterHelpers.BuildArgumentList(argNames);

        var modelArg = invocation.Model is not null ? $"\"{invocation.Model}\"" : "null";
        var providerConst = GetProviderConstant(invocation.Provider);
        var operationConst = GetOperationConstant(invocation.Operation);
        var elementType = ExtractStreamingElementType(returnType);

        sb.AppendLine($"// Intercepted streaming call at {displayLocation}");
        sb.AppendLine(interceptAttribute);
        sb.AppendLine($"public static {returnType} {methodName}({parameters})");
        using (sb.BeginBlock())
        {
            sb.AppendLine($"return GenAiInstrumentation.ExecuteStreamingAsync<{elementType}>(");
            sb.AppendLine($"    {providerConst},");
            sb.AppendLine($"    {operationConst},");
            sb.AppendLine($"    {modelArg},");
            sb.AppendLine($"    () => @this.{originalMethod}({arguments}));");
        }

        sb.AppendLine();
    }

    private static string ExtractStreamingElementType(string returnType)
    {
        var start = returnType.IndexOf('<');
        var end = returnType.LastIndexOf('>');
        return start < 0 || end < 0 || end <= start ? "object" : returnType[(start + 1)..end];
    }

    private static string? GetUsageExtractor(string provider, string operation)
    {
        var definition = ProviderRegistry.GenAiProviders.FirstOrDefault(p => p.ProviderId == provider);
        if (definition?.TokenUsage is null || !TokenUsageOperations.Contains(operation))
            return null;

        var usage = definition.TokenUsage;
        var outputTokens = InputOnlyOperations.Contains(operation) ? "0" : $"(int)(r.{usage.OutputProperty} ?? 0)";
        var inputTokens = $"(int)(r.{usage.InputProperty} ?? 0)";
        return $"static r => new TokenUsage({inputTokens}, {outputTokens})";
    }

    private static EquatableArray<string> GetPositionalNames(int count)
    {
        var names = new string[count];
        for (var i = 0; i < count; i++) names[i] = $"arg{i}";
        return names.ToEquatableArray();
    }

