namespace Qyl.Instrumentation.Generators.Emitters;

using Microsoft.CodeAnalysis.CSharp;
using Models;

/// <summary>
///     Emits interceptor source code for agent invocations detected by <see cref="CallSites.AgentCallSiteAnalyzer" />.
/// </summary>
internal static class AgentInterceptorEmitter
{
    public static string Emit(ImmutableArray<AgentCallSite> invocations)
    {
        if (invocations.IsEmpty)
            return string.Empty;

        var sb = new IndentedStringBuilder();

        sb.AppendLineNoIndent("// <auto-generated/>");
        sb.AppendLineNoIndent("#pragma warning disable");
        sb.AppendLine();
        sb.AppendLine("using System.Diagnostics;");
        sb.AppendLine("using Qyl.Instrumentation.Instrumentation;");
        sb.AppendLine("using qyl.contracts.Attributes;");
        sb.AppendLine();

        EmitterHelpers.AppendInterceptsLocationAttribute(sb);

        using (sb.BeginNamespace("Qyl.Instrumentation.Generators"))
        using (sb.BeginClass("file static", "AgentInterceptors"))
        {
            var index = 0;
            foreach (var invocation in invocations.OrderBy(static i => i.SortKey, StringComparer.Ordinal))
            {
                AppendSingleInterceptor(sb, invocation, index);
                index++;
            }
        }

        return sb.ToString();
    }

    private static void AppendSingleInterceptor(IndentedStringBuilder sb, AgentCallSite invocation, int index)
    {
        var displayLocation = invocation.Location.GetDisplayLocation();
        var interceptAttribute = invocation.Location.GetInterceptsLocationAttributeSyntax();
        var methodName = $"Intercept_Agent_{index}";
        var returnType = invocation.ReturnTypeName;
        var originalMethod = invocation.MethodName;

        var parameters = EmitterHelpers.BuildParameterList(
            invocation.ContainingTypeName, invocation.ParameterTypes, invocation.ParameterNames);
        var arguments = EmitterHelpers.BuildArgumentList(invocation.ParameterNames);

        var spanName = GetSpanName(invocation);
        var agentNameLiteral = invocation.AgentName is not null
            ? SymbolDisplay.FormatLiteral(invocation.AgentName, true)
            : "null";
        var operationConst = GetOperationConstant(invocation.Kind);

        var isAsync = invocation.IsAsync;
        var hasReturnValue = isAsync
            ? !returnType.EndsWithOrdinal("Task") && !returnType.EndsWithOrdinal("ValueTask")
            : returnType != "void";

        sb.AppendLine($"// Intercepted agent call at {displayLocation}");
        sb.AppendLine(interceptAttribute);

        var asyncMod = isAsync ? "async " : "";
        var awaitMod = isAsync ? "await " : "";

        sb.AppendLine($"public static {asyncMod}{returnType} {methodName}({parameters})");
        using (sb.BeginBlock())
        {
            sb.AppendLine("using var activity = ActivitySources.AgentSource.StartActivity(");
            sb.AppendLine($"    \"{spanName}\",");
            sb.AppendLine("    global::System.Diagnostics.ActivityKind.Client);");
            sb.AppendLine();

            using (sb.BeginIf("activity is not null"))
            {
                sb.AppendLine($"activity.SetTag(GenAiAttributes.OperationName, {operationConst});");
                sb.AppendLine(
                    "activity.SetTag(GenAiAttributes.ProviderName, GenAiAttributes.Providers.MicrosoftAgents);");
                using (sb.BeginIf($"{agentNameLiteral} is not null"))
                {
                    sb.AppendLine($"activity.SetTag(\"gen_ai.agent.name\", {agentNameLiteral});");
                }
            }

            sb.AppendLine();
            sb.AppendLine("try");
            using (sb.BeginBlock())
            {
                if (hasReturnValue)
                {
                    sb.AppendLine($"var result = {awaitMod}@this.{originalMethod}({arguments});");
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                    sb.AppendLine("return result;");
                }
                else
                {
                    sb.AppendLine($"{awaitMod}@this.{originalMethod}({arguments});");
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                }
            }

            sb.AppendLine("catch (global::System.Exception ex)");
            using (sb.BeginBlock())
            {
                sb.AppendLine(
                    "global::Qyl.Instrumentation.Instrumentation.ActivityExceptionTelemetry.Record(activity, ex);");
                sb.AppendLine("throw;");
            }
        }

        sb.AppendLine();
    }

    private static string GetSpanName(AgentCallSite invocation) =>
        invocation.Kind switch
        {
            AgentCallKind.BuilderRegistration => invocation.AgentName is not null
                ? $"create_agent {SymbolDisplay.FormatLiteral(invocation.AgentName, false)}"
                : "create_agent",
            _ => invocation.AgentName is not null
                ? $"invoke_agent {SymbolDisplay.FormatLiteral(invocation.AgentName, false)}"
                : "invoke_agent"
        };

    private static string GetOperationConstant(AgentCallKind kind) =>
        kind switch
        {
            AgentCallKind.BuilderRegistration => "GenAiAttributes.Operations.CreateAgent",
            _ => "GenAiAttributes.Operations.InvokeAgent"
        };
}
