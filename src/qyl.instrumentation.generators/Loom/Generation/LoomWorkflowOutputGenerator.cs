using Qyl.Instrumentation.Generators.Loom.Models;

namespace Qyl.Instrumentation.Generators.Loom.Generation;

internal static class LoomWorkflowOutputGenerator
{
    public static string Generate(LoomWorkflowModel workflow)
    {
        var sb = new IndentedStringBuilder();
        sb.Append(GeneratedCodeHelpers.AutoGeneratedHeader("Qyl.Instrumentation.Generators.Loom"));
        sb.AppendLine(GeneratedCodeHelpers.NullableEnable);
        sb.AppendLine();

        var namespaceName = LoomGenerationHelpers.GetNamespace(workflow.WorkflowTypeFullyQualified);
        if (!string.IsNullOrWhiteSpace(namespaceName))
        {
            sb.AppendLine($"namespace {namespaceName};");
            sb.AppendLine();
        }

        LoomGenerationHelpers.AppendDeclarationChain(sb, workflow.DeclarationChain);

        sb.AppendLine(
            "public static global::Qyl.Instrumentation.Instrumentation.Loom.LoomWorkflowDescriptor Descriptor { get; } = new(");
        using (sb.BeginBlock(null))
        {
            sb.AppendLine($"{LoomGenerationHelpers.StringLiteral(workflow.Id)},");
            sb.AppendLine($"{LoomGenerationHelpers.TypeOf(workflow.RunStateTypeFullyQualified)},");
            if (workflow.StepIds.IsEmpty)
                sb.AppendLine("global::System.Array.Empty<string>(),");
            else
                sb.AppendLine(
                    $"new string[] {{ {string.Join(", ", workflow.StepIds.Select(LoomGenerationHelpers.StringLiteral))} }},");

            sb.AppendLine($"{LoomGenerationHelpers.NullableStringLiteral(workflow.Description)}");
        }
        sb.AppendLine(");");

        for (var i = 0; i < workflow.DeclarationChain.Length; i++)
            sb.EndBlock();

        return sb.ToString();
    }
}
