namespace Qyl.Instrumentation.Generators.Loom.Generation;

using Models;

internal static class LoomStepOutputGenerator
{
    public static string Generate(LoomStepModel step)
    {
        var sb = new IndentedStringBuilder();
        sb.Append(GeneratedCodeHelpers.AutoGeneratedHeader("Qyl.Instrumentation.Generators.Loom"));
        sb.AppendLine(GeneratedCodeHelpers.NullableEnable);
        sb.AppendLine();

        var namespaceName = LoomGenerationHelpers.GetNamespace(step.ExecutorTypeFullyQualified);
        if (!string.IsNullOrWhiteSpace(namespaceName))
        {
            sb.AppendLine($"namespace {namespaceName};");
            sb.AppendLine();
        }

        LoomGenerationHelpers.AppendDeclarationChain(sb, step.DeclarationChain);

        sb.AppendLine(
            "public static global::Qyl.Instrumentation.Instrumentation.Loom.LoomStepDescriptor Descriptor { get; } = new(");
        sb.AppendLine($"{LoomGenerationHelpers.StringLiteral(step.Id)},");
        sb.AppendLine($"(global::Qyl.Instrumentation.Instrumentation.Loom.LoomPhase){step.Phase},");
        sb.AppendLine($"{LoomGenerationHelpers.TypeOf(step.ExecutorTypeFullyQualified)},");
        sb.AppendLine($"{LoomGenerationHelpers.NullableStringLiteral(step.Description)}");
        sb.AppendLine(");");

        for (var i = 0; i < step.DeclarationChain.Length; i++)
            sb.EndBlock();

        return sb.ToString();
    }
}
