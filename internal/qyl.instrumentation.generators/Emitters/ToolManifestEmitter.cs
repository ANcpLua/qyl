using Qyl.Instrumentation.Generators.Models;

namespace Qyl.Instrumentation.Generators.Emitters;

/// <summary>
///     Emits <c>QylToolManifest.ToolTypes</c> — a compile-time <c>Type[]</c> of all
///     [McpServerToolType]-decorated classes, replacing the hardcoded array in McpToolRegistry.
/// </summary>
internal static class ToolManifestEmitter
{
    public static string Emit(ImmutableArray<ToolTypeEntry> toolTypes)
    {
        if (toolTypes.IsEmpty)
            return string.Empty;

        var sb = new IndentedStringBuilder();

        sb.AppendLineNoIndent("// <auto-generated/>");
        sb.AppendLineNoIndent("#pragma warning disable");
        sb.AppendLine();

        using (sb.BeginNamespace("Qyl.Generated"))
        using (sb.BeginClass("internal static", "QylToolManifest"))
        {
            sb.AppendLine("public static readonly global::System.Type[] ToolTypes =");
            sb.AppendLine("{");

            foreach (var entry in toolTypes
                         .OrderBy(static e => e.FullyQualifiedTypeName, StringComparer.Ordinal))
            {
                sb.AppendLine($"    typeof({entry.FullyQualifiedTypeName}),");
            }

            sb.AppendLine("};");
        }

        return sb.ToString();
    }
}
