    private static void AppendMethodImplementation(
        IndentedStringBuilder sb,
        MetricMethodDefinition method,
        string fieldName)
    {
        if (IsObservable(method))
            return;

        var paramParts = new List<string>();
        var valueParameterName = method.ValueParameterName ?? "value";

        if (method.ValueTypeName is not null)
            paramParts.Add($"{method.ValueTypeName} {valueParameterName}");
        else if (method.Kind == MetricKind.Gauge)
            paramParts.Add("long value");

        foreach (var tag in method.Tags)
            paramParts.Add($"{tag.TypeName} {tag.ParameterName}");

        var paramList = string.Join(", ", paramParts);

        sb.AppendLine($"{method.Accessibility} static partial void {method.MethodName}({paramList})");
        using (sb.BeginBlock())
        {
            switch (method.Tags.Length)
            {
                case 0:
                    sb.AppendLine(method.Kind switch
                    {
                        MetricKind.Counter when method.ValueTypeName is not null => $"{fieldName}.Add({valueParameterName});",
                        MetricKind.Counter => $"{fieldName}.Add(1);",
                        MetricKind.UpDownCounter => $"{fieldName}.Add({valueParameterName});",
                        _ => $"{fieldName}.Record({valueParameterName});"
                    });
                    break;
                case 1:
                {
                    var tag = method.Tags[0];
                    var kvp =
                        $"new global::System.Collections.Generic.KeyValuePair<string, object?>({StringLiteral(tag.TagName)}, {tag.ParameterName})";

                    sb.AppendLine(method.Kind switch
                    {
                        MetricKind.Counter when method.ValueTypeName is not null =>
                            $"{fieldName}.Add({valueParameterName}, {kvp});",
                        MetricKind.Counter => $"{fieldName}.Add(1, {kvp});",
                        MetricKind.UpDownCounter => $"{fieldName}.Add({valueParameterName}, {kvp});",
                        _ => $"{fieldName}.Record({valueParameterName}, {kvp});"
                    });
                    break;
                }
                default:
                {
                    var tagList = method.Tags
                        .Select(static t => $"{{ {StringLiteral(t.TagName)}, {t.ParameterName} }}");

                    sb.AppendLine(
                        $"var tags = new global::System.Diagnostics.TagList {{ {string.Join(", ", tagList)} }};");

                    sb.AppendLine(method.Kind switch
                    {
                        MetricKind.Counter when method.ValueTypeName is not null =>
                            $"{fieldName}.Add({valueParameterName}, in tags);",
                        MetricKind.Counter => $"{fieldName}.Add(1, in tags);",
                        MetricKind.UpDownCounter => $"{fieldName}.Add({valueParameterName}, in tags);",
                        _ => $"{fieldName}.Record({valueParameterName}, in tags);"
                    });
                    break;
                }
            }
        }

        sb.AppendLine();
    }

    private static string BuildInstrumentArguments(MetricMethodDefinition method, string? callback = null)
    {
        var args = StringLiteral(method.MetricName);

        if (callback is not null)
            args += $", {callback}";

        if (method.Unit is not null || method.Description is not null)
        {
            var unitArg = method.Unit is not null ? StringLiteral(method.Unit) : "null";
            args += $", {unitArg}";
        }

        if (method.Description is not null)
            args += $", {StringLiteral(method.Description)}";

        return args;
    }

    private static string BuildObservableCallback(MetricMethodDefinition method, string valueType) =>
        method.CallbackKind switch
        {
            ObservableCallbackKind.Value =>
                $"new global::System.Func<{valueType}>({method.MethodName})",
            ObservableCallbackKind.Measurement =>
                $"new global::System.Func<global::System.Diagnostics.Metrics.Measurement<{valueType}>>({method.MethodName})",
            ObservableCallbackKind.Measurements =>
                $"new global::System.Func<global::System.Collections.Generic.IEnumerable<global::System.Diagnostics.Metrics.Measurement<{valueType}>>>({method.MethodName})",
            _ => method.MethodName
        };

    private static void AppendObservableInitializationMethod(
        IndentedStringBuilder sb,
        EquatableArray<MetricMethodDefinition> methods,
        IReadOnlyList<string> fieldNames,
        bool emitModuleInitializer)
    {
        if (emitModuleInitializer)
            sb.AppendLine("[global::System.Runtime.CompilerServices.ModuleInitializer]");

        sb.AppendLine($"internal static void {ObservableInitializerMethodName}()");
        using (sb.BeginBlock())
        {
            for (var i = 0; i < methods.Length; i++)
            {
                if (IsObservable(methods[i]))
                    sb.AppendLine($"_ = {fieldNames[i]};");
            }
        }
    }

    private static void AppendObservableInitializerForwarder(
        IndentedStringBuilder sb,
        string methodName,
        string childTypeName,
        string childInitializerName,
        bool emitModuleInitializer)
    {
        sb.AppendLine();
        if (emitModuleInitializer)
            sb.AppendLine("[global::System.Runtime.CompilerServices.ModuleInitializer]");

        sb.AppendLine($"internal static void {methodName}()");
        using (sb.BeginBlock())
        {
            sb.AppendLine($"{childTypeName}.{childInitializerName}();");
        }
    }

    private static string BuildObservableForwarderMethodName(MeterDefinition meter)
    {
        var qualifiedName = BuildQualifiedMeterClassName(meter);
        return string.Concat(
            ObservableInitializerMethodName,
            "_",
            ToIdentifierSuffix(qualifiedName),
            "_",
            StableHash(qualifiedName));
    }

    private static string BuildQualifiedMeterClassName(MeterDefinition meter)
    {
        var builder = new StringBuilder();

        if (!IsGlobalNamespace(meter.Namespace))
            builder.Append(meter.Namespace).Append('.');

        foreach (var containingType in meter.ContainingTypes)
            builder.Append(containingType.ClassName).Append('.');

        builder.Append(meter.ClassName);
        return builder.ToString();
    }

    private static string ToIdentifierSuffix(string value) =>
        value.Length is 0 ? "Meter" : value.SanitizeIdentifier();

    private static string StableHash(string value)
    {
        const uint offsetBasis = 2166136261;
        const uint prime = 16777619;

        var hash = offsetBasis;
        foreach (var ch in value)
        {
            hash ^= ch;
            hash *= prime;
        }

        return hash.ToString("x8", CultureInfo.InvariantCulture);
    }

    private static bool IsGlobalNamespace(string namespaceName) =>
        string.IsNullOrEmpty(namespaceName) ||
        string.Equals(namespaceName, "<global namespace>", StringComparison.Ordinal);

    private static bool IsObservable(MetricMethodDefinition method) =>
        method.Kind is MetricKind.ObservableCounter or MetricKind.ObservableGauge or MetricKind.ObservableUpDownCounter;

    private static string ToFieldName(string metricName)
    {
        var result = new StringBuilder("_");
        var upperNext = false;

        foreach (var ch in metricName)
        {
            if (!char.IsLetterOrDigit(ch) && ch != '_')
            {
                upperNext = true;
                continue;
            }

            result.Append(upperNext ? char.ToUpperInvariant(ch) : ch);
            upperNext = false;
        }

        return result.Length > 1 ? result.ToString() : "_metric";
    }

    private static string StringLiteral(string value)
    {
        var builder = new StringBuilder(value.Length + 2);
        builder.Append('"');

        foreach (var ch in value)
        {
            switch (ch)
            {
                case '"':
                    builder.Append("\\\"");
                    break;
                case '\\':
                    builder.Append("\\\\");
                    break;
                case '\0':
                    builder.Append("\\0");
                    break;
                case '\a':
                    builder.Append("\\a");
                    break;
                case '\b':
                    builder.Append("\\b");
                    break;
                case '\f':
                    builder.Append("\\f");
                    break;
                case '\n':
                    builder.Append("\\n");
                    break;
                case '\r':
                    builder.Append("\\r");
                    break;
                case '\t':
                    builder.Append("\\t");
                    break;
                case '\v':
                    builder.Append("\\v");
                    break;
                default:
                    if (char.IsControl(ch) || ch > '\x7e')
                    {
                        builder.Append("\\u");
                        builder.Append(((int)ch).ToString("x4", CultureInfo.InvariantCulture));
                    }
                    else
                    {
                        builder.Append(ch);
                    }
                    break;
            }
        }

        builder.Append('"');
        return builder.ToString();
    }

