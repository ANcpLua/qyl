    private const string ObservableInitializerMethodName = "__QylInitializeObservableInstruments";

    public static string Emit(ImmutableArray<MeterDefinition> meters)
    {
        if (meters.IsEmpty)
            return string.Empty;

        var sb = new IndentedStringBuilder();

        sb.AppendLineNoIndent("// <auto-generated/>");
        sb.AppendLineNoIndent(GeneratedCodeHelpers.NullableEnable);
        sb.AppendLine();

        foreach (var meterName in meters
                     .Select(static meter => meter.MeterName)
                     .Distinct(StringComparer.Ordinal)
                     .OrderBy(static name => name, StringComparer.Ordinal))
        {
            sb.AppendLine(
                $"[assembly: global::Qyl.Instrumentation.GeneratedMeterAttribute({StringLiteral(meterName)})]");
        }

        sb.AppendLine();

        foreach (var meter in meters.OrderBy(static m => m.SortKey, StringComparer.Ordinal))
            AppendMeterClass(sb, meter);

        return sb.ToString();
    }

    private static void AppendMeterClass(IndentedStringBuilder sb, MeterDefinition meter)
    {
        if (IsGlobalNamespace(meter.Namespace))
        {
            AppendMeterClassInCurrentScope(sb, meter);
            return;
        }

        using (sb.BeginNamespace(meter.Namespace))
        {
            AppendMeterClassInCurrentScope(sb, meter);
        }
    }

    private static void AppendMeterClassInCurrentScope(IndentedStringBuilder sb, MeterDefinition meter)
    {
        if (meter.ContainingTypes.Length is 0)
        {
            using (BeginMeterClass(sb, meter.ClassModifiers, meter.ClassName))
            {
                AppendMeterClassMembers(sb, meter, emitModuleInitializer: true);
            }

            return;
        }

        var forwarderName = BuildObservableForwarderMethodName(meter);
        AppendContainingType(sb, meter, containingTypeIndex: 0, forwarderName);
    }

    private static bool AppendContainingType(
        IndentedStringBuilder sb,
        MeterDefinition meter,
        int containingTypeIndex,
        string forwarderName)
    {
        var containingType = meter.ContainingTypes[containingTypeIndex];
        using (BeginMeterClass(sb, containingType.ClassModifiers, containingType.ClassName))
        {
            string childTypeName;
            string childInitializerName;
            bool hasObservableInstruments;

            if (containingTypeIndex == meter.ContainingTypes.Length - 1)
            {
                childTypeName = meter.ClassName;
                childInitializerName = ObservableInitializerMethodName;
                using (BeginMeterClass(sb, meter.ClassModifiers, meter.ClassName))
                {
                    hasObservableInstruments = AppendMeterClassMembers(sb, meter, emitModuleInitializer: false);
                }
            }
            else
            {
                var childContainingType = meter.ContainingTypes[containingTypeIndex + 1];
                childTypeName = childContainingType.ClassName;
                childInitializerName = forwarderName;
                hasObservableInstruments = AppendContainingType(
                    sb,
                    meter,
                    containingTypeIndex + 1,
                    forwarderName);
            }

            if (hasObservableInstruments)
            {
                var emitModuleInitializer = containingTypeIndex is 0;
                AppendObservableInitializerForwarder(
                    sb,
                    forwarderName,
                    childTypeName,
                    childInitializerName,
                    emitModuleInitializer);
            }

            return hasObservableInstruments;
        }
    }

    private static IDisposable BeginMeterClass(
        IndentedStringBuilder sb,
