        return sb.BeginClass(modifiers, className);
    }

    private static bool AppendMeterClassMembers(
        IndentedStringBuilder sb,
        MeterDefinition meter,
        bool emitModuleInitializer)
    {
        var meterName = StringLiteral(meter.MeterName);
        var meterCtor = meter.MeterVersion is not null
            ? $"new global::System.Diagnostics.Metrics.Meter({meterName}, {StringLiteral(meter.MeterVersion)})"
            : $"new global::System.Diagnostics.Metrics.Meter({meterName})";

        sb.AppendLine($"private static readonly global::System.Diagnostics.Metrics.Meter _meter = {meterCtor};");
        sb.AppendLine();

        var fieldNames = BuildInstrumentFieldNames(meter.Methods);
        var hasObservableInstruments = false;
        for (var i = 0; i < meter.Methods.Length; i++)
        {
            hasObservableInstruments |= IsObservable(meter.Methods[i]);
            AppendInstrumentField(sb, meter.Methods[i], fieldNames[i]);
        }

        if (hasObservableInstruments)
        {
            sb.AppendLine();
            AppendObservableInitializationMethod(sb, meter.Methods, fieldNames, emitModuleInitializer);
        }

        sb.AppendLine();

        for (var i = 0; i < meter.Methods.Length; i++)
            AppendMethodImplementation(sb, meter.Methods[i], fieldNames[i]);
        return hasObservableInstruments;
    }

    private static List<string> BuildInstrumentFieldNames(EquatableArray<MetricMethodDefinition> methods)
    {
        var fieldNames = new List<string>(methods.Length);
        var usedNames = new HashSet<string>(StringComparer.Ordinal);

        for (var i = 0; i < methods.Length; i++)
        {
            var baseName = ToFieldName(methods[i].MetricName);
            var fieldName = baseName;
            var suffix = 2;

            while (!usedNames.Add(fieldName))
            {
                fieldName = string.Concat(baseName, suffix.ToString(CultureInfo.InvariantCulture));
                suffix++;
            }

            fieldNames.Add(fieldName);
        }

        return fieldNames;
    }

    private static void AppendInstrumentField(
        IndentedStringBuilder sb,
        MetricMethodDefinition method,
        string fieldName)
    {
        switch (method.Kind)
        {
            case MetricKind.Counter:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method);
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.Counter<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateCounter<{valueType}>({args});");
                break;
            }

            case MetricKind.Histogram:
            {
                var valueType = method.ValueTypeName ?? "double";
                var args = BuildInstrumentArguments(method);
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.Histogram<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateHistogram<{valueType}>({args});");
                break;
            }

            case MetricKind.Gauge:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method);
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.Gauge<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateGauge<{valueType}>({args});");
                break;
            }

            case MetricKind.UpDownCounter:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method);
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.UpDownCounter<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateUpDownCounter<{valueType}>({args});");
                break;
            }
            case MetricKind.ObservableCounter:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method, BuildObservableCallback(method, valueType));
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.ObservableCounter<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateObservableCounter<{valueType}>({args});");
                break;
            }
            case MetricKind.ObservableGauge:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method, BuildObservableCallback(method, valueType));
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.ObservableGauge<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateObservableGauge<{valueType}>({args});");
                break;
            }
            case MetricKind.ObservableUpDownCounter:
            {
                var valueType = method.ValueTypeName ?? "long";
                var args = BuildInstrumentArguments(method, BuildObservableCallback(method, valueType));
                sb.AppendLine(
                    $"private static readonly global::System.Diagnostics.Metrics.ObservableUpDownCounter<{valueType}> {fieldName} =");
                sb.AppendLine($"    _meter.CreateObservableUpDownCounter<{valueType}>({args});");
                break;
            }
            default:
                throw new InvalidOperationException($"Unsupported MetricKind: {method.Kind}");
        }
