    public static string Emit(ImmutableArray<TracedCallSite> invocations)
    {
        if (invocations.IsEmpty)
            return string.Empty;

        var fieldNames = BuildFieldNameMap(invocations);
        var sb = new IndentedStringBuilder();

        sb.AppendLineNoIndent("// <auto-generated/>");
        sb.AppendLineNoIndent("#pragma warning disable");
        sb.AppendLine();
        sb.AppendLine("using System.Diagnostics;");
        sb.AppendLine();

        AppendGeneratedActivitySourceAttributes(sb, invocations);
        EmitterHelpers.AppendInterceptsLocationAttribute(sb);
        AppendActivitySourcesClass(sb, fieldNames);

        using (sb.BeginNamespace("Qyl.Instrumentation.Generators"))
        using (sb.BeginClass("file static", "TracedInterceptors"))
        {
            var index = 0;
            foreach (var cs in invocations.OrderBy(static cs => cs.SortKey, StringComparer.Ordinal))
            {
                AppendInterceptor(sb, cs, index++, fieldNames);
            }
        }

        return sb.ToString();
    }

    private static void AppendGeneratedActivitySourceAttributes(
        IndentedStringBuilder sb, ImmutableArray<TracedCallSite> invocations)
    {
        foreach (var activitySourceName in invocations
                     .Select(static invocation => invocation.ActivitySourceName)
                     .Distinct(StringComparer.Ordinal)
                     .OrderBy(static name => name, StringComparer.Ordinal))
        {
            sb.AppendLine(
                $"[assembly: global::Qyl.Instrumentation.GeneratedActivitySourceAttribute({SymbolDisplay.FormatLiteral(activitySourceName, true)})]");
        }

        sb.AppendLine();
    }

    private static Dictionary<string, string> BuildFieldNameMap(ImmutableArray<TracedCallSite> invocations)
    {
        var map = new Dictionary<string, string>(StringComparer.Ordinal);
        var used = new HashSet<string>(StringComparer.Ordinal);
        foreach (var name in invocations.Select(static i => i.ActivitySourceName).Distinct(StringComparer.Ordinal)
                     .OrderBy(static n => n, StringComparer.Ordinal))
        {
            var field = Sanitize(name);
            var candidate = field;
            var n = 1;
            while (!used.Add(candidate)) candidate = $"{field}_{n++}";
            map[name] = candidate;
        }

        return map;
    }

    private static string Sanitize(string name)
    {
        var sb = new StringBuilder(name.Length);
        foreach (var c in name) sb.Append(char.IsLetterOrDigit(c) ? c : '_');
        return sb.ToString();
    }

    private static void AppendActivitySourcesClass(IndentedStringBuilder sb, Dictionary<string, string> fieldNames)
    {
        using (sb.BeginNamespace("Qyl.Instrumentation.Generators"))
        using (sb.BeginClass("file static", "TracedActivitySources"))
        {
            foreach (var kv in fieldNames.OrderBy(static kv => kv.Key, StringComparer.Ordinal))
            {
                sb.AppendLine(
                    $"internal static readonly global::System.Diagnostics.ActivitySource {kv.Value} = new(\"{kv.Key}\");");
            }
        }

        sb.AppendLine();
    }

    private static void AppendInterceptor(
        IndentedStringBuilder sb, TracedCallSite cs, int index, Dictionary<string, string> fieldNames)
    {
        var typeParamNames = cs.TypeParameters.Select(static tp => tp.Name).ToArray().ToEquatableArray();
        var returnType = cs.ReturnTypeName.ToGlobalTypeName(
            typeParamNames.IsDefaultOrEmpty ? null : typeParamNames.AsImmutableArray());
        var typeParams = cs.TypeParameters.Length is 0
            ? ""
            : "<" + string.Join(", ", cs.TypeParameters.Select(static tp => tp.Name)) + ">";
        var constraints = cs.TypeParameters.Length is 0
            ? ""
            : " " + string.Join(" ",
                cs.TypeParameters.Where(static tp => tp.Constraints is not null).Select(static tp => tp.Constraints!));
        var parameters = EmitterHelpers.BuildParameterList(
            cs.ContainingTypeName, cs.ParameterTypes, cs.ParameterNames, cs.IsStatic, typeParamNames);
        var arguments = EmitterHelpers.BuildArgumentList(cs.ParameterNames);
        var field = fieldNames.TryGetValue(cs.ActivitySourceName, out var f) ? f : Sanitize(cs.ActivitySourceName);
        var call = cs.IsStatic
            ? $"global::{cs.ContainingTypeName}.{cs.MethodName}{typeParams}({arguments})"
            : $"@this.{cs.MethodName}{typeParams}({arguments})";

        var display = cs.Location.GetDisplayLocation();
        var intercept = cs.Location.GetInterceptsLocationAttributeSyntax();
        var startActivity = BuildStartActivity(field, cs);

        sb.AppendLine($"// {display}");
        sb.AppendLine(intercept);

        if (cs.IsAsyncEnumerable)
        {
            AppendAsyncEnumerableBody(sb, cs, index, typeParams, constraints, returnType, parameters, startActivity,
                call);
        }
        else if (cs.IsAsync)
            AppendAsyncBody(sb, cs, index, typeParams, constraints, returnType, parameters, startActivity, call);
        else
            AppendSyncBody(sb, cs, index, typeParams, constraints, returnType, parameters, startActivity, call);
    }

    private static void AppendSyncBody(
        IndentedStringBuilder sb, TracedCallSite cs, int index,
        string typeParams, string constraints, string returnType, string parameters,
        string startActivity, string call)
    {
        var hasReturn = returnType != "void";
        sb.AppendLine($"public static {returnType} Intercept_Traced_{index}{typeParams}({parameters}){constraints}");
        using (sb.BeginBlock())
        {
            sb.AppendLine(startActivity);
            AppendTagSetters(sb, cs);
            sb.AppendLine("try");
            using (sb.BeginBlock())
            {
                if (hasReturn)
                {
                    sb.AppendLine($"var result = {call};");
                    AppendReturnCapture(sb, cs);
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                    sb.AppendLine("return result;");
                }
                else
                {
                    sb.AppendLine($"{call};");
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                }
            }

            sb.AppendLine("catch (global::System.Exception ex)");
            using (sb.BeginBlock())
            {
                sb.AppendLine(
                    "global::Qyl.Instrumentation.Instrumentation.ActivityExceptionTelemetry.Record(activity, ex);");
                sb.AppendLine("throw;");
            }
        }

        sb.AppendLine();
    }

    private static void AppendAsyncBody(
        IndentedStringBuilder sb, TracedCallSite cs, int index,
        string typeParams, string constraints, string returnType, string parameters,
        string startActivity, string call)
    {
        var hasReturn = !returnType.EndsWithOrdinal("Task") && !returnType.EndsWithOrdinal("ValueTask");
        sb.AppendLine(
            $"public static async {returnType} Intercept_Traced_{index}{typeParams}({parameters}){constraints}");
        using (sb.BeginBlock())
        {
            sb.AppendLine(startActivity);
            AppendTagSetters(sb, cs);
            sb.AppendLine("try");
            using (sb.BeginBlock())
            {
                if (hasReturn)
                {
                    sb.AppendLine($"var result = await {call};");
                    AppendReturnCapture(sb, cs);
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                    sb.AppendLine("return result;");
                }
                else
                {
                    sb.AppendLine($"await {call};");
                    sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
                }
            }

            sb.AppendLine("catch (global::System.Exception ex)");
            using (sb.BeginBlock())
            {
                sb.AppendLine(
                    "global::Qyl.Instrumentation.Instrumentation.ActivityExceptionTelemetry.Record(activity, ex);");
                sb.AppendLine("throw;");
            }
        }

        sb.AppendLine();
    }

    private static void AppendAsyncEnumerableBody(
        IndentedStringBuilder sb, TracedCallSite cs, int index,
        string typeParams, string constraints, string returnType, string parameters,
        string startActivity, string call)
    {
        sb.AppendLine(
            $"public static async {returnType} Intercept_Traced_{index}{typeParams}({parameters}){constraints}");
        using (sb.BeginBlock())
        {
            sb.AppendLine(startActivity);
            AppendTagSetters(sb, cs);
            sb.AppendLine("try");
            using (sb.BeginBlock())
            {
                sb.AppendLine($"await foreach (var __item in {call})");
                sb.AppendLine("    yield return __item;");
                sb.AppendLine("activity?.SetStatus(global::System.Diagnostics.ActivityStatusCode.Ok);");
            }

            sb.AppendLine("catch (global::System.Exception ex)");
            using (sb.BeginBlock())
            {
                sb.AppendLine(
                    "global::Qyl.Instrumentation.Instrumentation.ActivityExceptionTelemetry.Record(activity, ex);");
                sb.AppendLine("throw;");
            }
        }

        sb.AppendLine();
    }

    private static string BuildStartActivity(string field, TracedCallSite cs) =>
        cs.RootSpan
            ? $"using var activity = TracedActivitySources.{field}.StartActivity(\"{cs.SpanName}\", global::System.Diagnostics.ActivityKind.{cs.SpanKind}, parentContext: default);"
            : $"using var activity = TracedActivitySources.{field}.StartActivity(\"{cs.SpanName}\", global::System.Diagnostics.ActivityKind.{cs.SpanKind});";

    private static void AppendTagSetters(IndentedStringBuilder sb, TracedCallSite cs)
    {
        var hasTags = cs.TracedTags.Length > 0 || cs.TracedTagProperties.Length > 0 ||
                      cs.CodeFilePath is { Length: > 0 } || cs.MethodName is { Length: > 0 };
        if (!hasTags) return;

        using (sb.BeginIf("activity is not null"))
        {
            if (cs.CodeFilePath is { Length: > 0 })
            {
                sb.AppendLine(
                    $"activity.SetTag(\"code.filepath\", {SymbolDisplay.FormatLiteral(cs.CodeFilePath, true)});");
            }

            sb.AppendLine($"activity.SetTag(\"code.function.name\", \"{cs.MethodName}\");");
            if (cs.CodeNamespace is { Length: > 0 })
                sb.AppendLine($"activity.SetTag(\"code.namespace\", \"{cs.CodeNamespace}\");");
            if (cs.CodeLineNumber > 0)
                sb.AppendLine($"activity.SetTag(\"code.lineno\", {cs.CodeLineNumber});");

            foreach (var tag in cs.TracedTags)
            {
                switch (tag)
                {
                    case { SkipIfDefault: true, IsValueType: true }:
                    {
                        var globalType = tag.TypeName.ToGlobalTypeName();
                        sb.AppendLine(
                            $"if (!global::System.Collections.Generic.EqualityComparer<{globalType}>.Default.Equals({tag.ParameterName}, default))");
                        sb.AppendLine($"    activity.SetTag(\"{tag.TagName}\", {tag.ParameterName});");
                        break;
                    }
                    case { SkipIfNull: true, IsNullable: true }:
                        sb.AppendLine($"if ({tag.ParameterName} is not null)");
                        sb.AppendLine($"    activity.SetTag(\"{tag.TagName}\", {tag.ParameterName});");
                        break;
                    default:
                        sb.AppendLine($"activity.SetTag(\"{tag.TagName}\", {tag.ParameterName});");
                        break;
                }
            }

            foreach (var prop in cs.TracedTagProperties)
            {
                var accessor = prop.IsStatic
                    ? $"global::{cs.ContainingTypeName}.{prop.PropertyName}"
                    : $"@this.{prop.PropertyName}";

                if (prop is { SkipIfNull: true, IsNullable: true })
                {
                    sb.AppendLine($"if ({accessor} is not null)");
                    sb.AppendLine($"    activity.SetTag(\"{prop.TagName}\", {accessor});");
                }
                else
                {
                    sb.AppendLine($"activity.SetTag(\"{prop.TagName}\", {accessor});");
                }
            }
        }
    }

    private static void AppendReturnCapture(IndentedStringBuilder sb, TracedCallSite cs)
    {
        if (cs.ReturnCapture is not { } capture) return;
        var accessor = capture.PropertyPath is { Length: > 0 }
            ? "result?." + string.Join("?.", capture.PropertyPath.Split('.'))
            : "result?.ToString()";
        sb.AppendLine($"activity?.SetTag(\"{capture.TagName}\", {accessor});");
    }
