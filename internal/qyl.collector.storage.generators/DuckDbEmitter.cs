    private static string BuildReaderAccessExpr(DuckDbColumnInfo col, int ordinal)
    {
        var baseType = col.PropertyType.TrimEnd('?');

        if (col.IsNullable)
            return baseType switch
            {
                "string" => $"DuckDbValueReader.ReadString(reader, {ordinal})",
                "ulong" or "System.UInt64" => $"DuckDbValueReader.ReadUInt64(reader, {ordinal})",
                "long" or "System.Int64" => $"DuckDbValueReader.ReadInt64(reader, {ordinal})",
                "double" or "System.Double" => $"DuckDbValueReader.ReadDouble(reader, {ordinal})",
                "decimal" or "System.Decimal" => $"reader.IsDBNull({ordinal}) ? null : reader.GetDecimal({ordinal})",
                "int" or "System.Int32" => $"DuckDbValueReader.ReadInt32(reader, {ordinal})",
                "byte" or "System.Byte" => $"DuckDbValueReader.ReadByte(reader, {ordinal})",
                "System.DateTimeOffset" or "DateTimeOffset" => $"DuckDbValueReader.ReadDateTimeOffset(reader, {ordinal})",
                _ => $"reader.IsDBNull({ordinal}) ? default : reader.GetValue({ordinal})"
            };

        return baseType switch
        {
            "string" => $"DuckDbValueReader.ReadString(reader, {ordinal}, \"\")",
            "ulong" or "System.UInt64" => $"DuckDbValueReader.ReadUInt64(reader, {ordinal}, 0UL)",
            "long" or "System.Int64" => $"DuckDbValueReader.ReadInt64(reader, {ordinal}, 0L)",
            "double" or "System.Double" => $"DuckDbValueReader.ReadDouble(reader, {ordinal}, 0d)",
            "decimal" or "System.Decimal" => $"reader.GetDecimal({ordinal})",
            "int" or "System.Int32" => $"DuckDbValueReader.ReadInt32(reader, {ordinal}, 0)",
            "byte" or "System.Byte" => $"DuckDbValueReader.ReadByte(reader, {ordinal}, 0)",
            "System.DateTimeOffset" or "DateTimeOffset" => $"DuckDbValueReader.ReadDateTimeOffset(reader, {ordinal}) ?? default",
            _ => $"reader.IsDBNull({ordinal}) ? default : reader.GetValue({ordinal})"
        };
    }

    private static void EmitBuildMultiRowInsertSql(IndentedStringBuilder sb, DuckDbTableInfo table)
    {
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Builds a multi-row INSERT statement with positional parameters.");
        sb.AppendLine("/// Example: INSERT INTO table (col1, col2) VALUES ($1, $2), ($3, $4)");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("public static string BuildMultiRowInsertSql(int rowCount)");
        sb.BeginBlock();

        sb.AppendLine("return s_insertSqlCache.GetOrAdd(rowCount, static count =>");
        sb.BeginBlock();

        sb.AppendLine("var sb = new StringBuilder(1024);");
        sb.AppendLine(
            "sb.Append(\"INSERT INTO \").Append(QuotedTableName).Append(\" (\").Append(ColumnList).Append(\") VALUES \");");
        sb.AppendLine();

        sb.AppendLine("for (var i = 0; i < count; i++)");
        sb.BeginBlock();
        sb.AppendLine("if (i > 0) sb.Append(\", \");");
        sb.AppendLine();
        sb.AppendLine("var baseParam = i * ColumnCount;");
        sb.AppendLine("sb.Append('(');");
        sb.AppendLine("for (var col = 0; col < ColumnCount; col++)");
        sb.BeginBlock();
        sb.AppendLine("if (col > 0) sb.Append(\", \");");
        sb.AppendLine("sb.Append('$').Append(baseParam + col + 1);");
        sb.EndBlock();
        sb.AppendLine("sb.Append(')');");
        sb.EndBlock();

        sb.AppendLine();

        if (!string.IsNullOrEmpty(table.OnConflict))
        {
            sb.AppendLine("// ON CONFLICT clause for upsert behavior");
            sb.AppendLine("sb.Append(\" \").Append(\"\"\"");

