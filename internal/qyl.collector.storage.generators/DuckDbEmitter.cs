                    continue;

                sb.AppendLineNoIndent($"                {line.Trim()}");
            }

            sb.AppendLineNoIndent("                \"\"\");");
        }

        sb.AppendLine();
        sb.AppendLine("return sb.ToString();");
        sb.EndBlock("});");

        sb.EndBlock();
    }
