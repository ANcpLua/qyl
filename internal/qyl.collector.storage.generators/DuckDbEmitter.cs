        sb.AppendLineNoIndent(line.ToString());
    }

    private static void EmitCreateTableDdl(IndentedStringBuilder sb, DuckDbTableInfo table)
    {
        sb.AppendLine("public const string CreateTableDdl = \"\"\"");
        sb.AppendLineNoIndent($"        CREATE TABLE IF NOT EXISTS {SqlIdentifier.Quote(table.TableName)} (");

        var primaryKeyColumns = table.Columns
            .Where(static c => c.PrimaryKeyOrdinal >= 0)
            .OrderBy(static c => c.PrimaryKeyOrdinal)
            .ToArray();

        for (var i = 0; i < table.Columns.Length; i++)
        {
            var column = table.Columns[i];
            var line = new StringBuilder("            ");
            line.Append(SqlIdentifier.Quote(column.ColumnName)).Append(' ').Append(ResolveSqlType(column));

            if (!column.IsNullable)
                line.Append(" NOT NULL");

            if (!string.IsNullOrWhiteSpace(column.DefaultSql))
                line.Append(" DEFAULT ").Append(column.DefaultSql);

            if (i < table.Columns.Length - 1 || primaryKeyColumns.Length > 0)
                line.Append(',');

            sb.AppendLineNoIndent(line.ToString());
        }

        if (primaryKeyColumns.Length > 0)
        {
            var line = new StringBuilder("            PRIMARY KEY (");
            for (var i = 0; i < primaryKeyColumns.Length; i++)
            {
                if (i > 0)
                    line.Append(", ");

                line.Append(SqlIdentifier.Quote(primaryKeyColumns[i].ColumnName));
            }

            line.Append(')');
            sb.AppendLineNoIndent(line.ToString());
        }

        sb.AppendLineNoIndent("        );");
        sb.AppendLineNoIndent("        \"\"\";");
        sb.AppendLine();
    }

    // One ALTER TABLE ... ADD COLUMN IF NOT EXISTS per column: run at startup after CREATE TABLE
    // IF NOT EXISTS (and before index DDL) so a database persisted by an older schema gains any
    // columns added since. Migration-added columns are always nullable — NOT NULL cannot hold for
    // pre-existing rows — and readers already map SQL NULL to CLR defaults for non-nullable
    // properties. New databases get the full constrained schema from CreateTableDdl; every ALTER
    // then no-ops.
    private static void EmitMigrateTableDdl(IndentedStringBuilder sb, DuckDbTableInfo table)
    {
        sb.AppendLine("public const string MigrateTableDdl = \"\"\"");

        foreach (var column in table.Columns)
        {
            var line = new StringBuilder("        ALTER TABLE ");
            line.Append(SqlIdentifier.Quote(table.TableName))
                .Append(" ADD COLUMN IF NOT EXISTS ")
                .Append(SqlIdentifier.Quote(column.ColumnName))
                .Append(' ')
                .Append(ResolveSqlType(column));

            if (!string.IsNullOrWhiteSpace(column.DefaultSql))
                line.Append(" DEFAULT ").Append(column.DefaultSql);

            line.Append(';');
            sb.AppendLineNoIndent(line.ToString());
        }

        sb.AppendLineNoIndent("        \"\"\";");
        sb.AppendLine();
    }

    private static void EmitIndexesDdl(IndentedStringBuilder sb, DuckDbTableInfo table)
    {
        if (table.Indexes.Length is 0)
        {
            sb.AppendLine("public const string IndexesDdl = \"\";");
            sb.AppendLine();
            return;
        }

        sb.AppendLine("public const string IndexesDdl = \"\"\"");

        foreach (var index in table.Indexes)
        {
            var line = new StringBuilder("        CREATE INDEX IF NOT EXISTS ");
            line.Append(SqlIdentifier.Quote(index.Name))
                .Append(" ON ")
                .Append(SqlIdentifier.Quote(table.TableName))
                .Append('(');

            for (var i = 0; i < index.ColumnNames.Length; i++)
            {
                if (i > 0)
                    line.Append(", ");

                line.Append(SqlIdentifier.Quote(index.ColumnNames[i]));
            }

            line.Append(");");
            sb.AppendLineNoIndent(line.ToString());
        }

        sb.AppendLineNoIndent("        \"\"\";");
        sb.AppendLine();
    }

    private static string ResolveSqlType(DuckDbColumnInfo column)
    {
        if (!string.IsNullOrWhiteSpace(column.SqlType))
            return column.SqlType!;

        var type = column.PropertyType.TrimEnd('?');
        if (column.IsUBigInt)
            return "UBIGINT";

        return type switch
        {
            "string" or "System.String" => "VARCHAR",
            "byte" or "System.Byte" => "TINYINT",
            "int" or "System.Int32" => "INTEGER",
            "long" or "System.Int64" => "BIGINT",
            "ulong" or "System.UInt64" => "UBIGINT",
            "double" or "System.Double" => "DOUBLE",
            "decimal" or "System.Decimal" => "DECIMAL",
            "bool" or "System.Boolean" => "BOOLEAN",
            "System.DateTimeOffset" or "DateTimeOffset" => "TIMESTAMP",
            _ => throw new InvalidOperationException(
                $"No DuckDB SQL type mapping for {column.PropertyType} on {column.PropertyName}. Set SqlType explicitly.")
        };
    }

    private static string EscapeCSharpString(string value) =>
        value.Replace("\\", "\\\\").Replace("\"", "\\\"");

    private static void EmitAddParameters(IndentedStringBuilder sb, string typeName, DuckDbColumnInfo[] columns)
    {
        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Adds parameters for INSERT in column order.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("[MethodImpl(MethodImplOptions.AggressiveInlining)]");
        sb.AppendLine($"public static void AddParameters(DuckDBCommand cmd, {typeName} row)");
        sb.BeginBlock();

        foreach (var col in columns)
            sb.AppendLine($"cmd.Parameters.Add(new DuckDBParameter {{ Value = {BuildParameterValueExpr(col)} }});");

        sb.EndBlock();
        sb.AppendLine();
    }

    private static string BuildParameterValueExpr(DuckDbColumnInfo col)
    {
        var value = $"row.{col.PropertyName}";

        if (col.IsUBigInt)
            return col.IsNullable
                ? $"{value}.HasValue ? (object)(decimal){value}.Value : DBNull.Value"
                : $"(decimal){value}";

        return col.IsNullable
            ? $"{value} ?? (object)DBNull.Value"
            : value;
    }

