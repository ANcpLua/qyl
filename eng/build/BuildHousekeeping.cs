// Targets adapted from open-telemetry/opentelemetry-dotnet-instrumentation (Apache-2.0):
// their sdk-version drift gate and dependency-list generation, rehosted on qyl's
// eng/tools ports of SdkVersionAnalyzer, DependencyListGenerator, and LibraryVersionsGenerator.

using System;
using System.Globalization;
using System.IO;
using System.Text;
using DependencyListGenerator;
using Nuke.Common;
using Nuke.Common.IO;
using Nuke.Common.Tooling;
using Nuke.Common.Tools.DotNet;
using Serilog;

namespace Qyl.Build;

[ParameterPrefix(nameof(IHousekeeping))]
interface IHousekeeping : IHazSourcePaths
{
    AbsolutePath ToolsDirectory => RootDirectory / "eng" / "tools";

    AbsolutePath DependencyListFile => RootDirectory / "docs" / "dependencies.md";

    [Parameter("Requested .NET 10 SDK version for UpdateSdkVersions (e.g. 10.0.302)")]
    string? SdkVersion => TryGetValue(() => SdkVersion);

    Target VerifySdkVersions => d => d
        .Description("Verify pinned .NET SDK versions in workflows and dockerfiles match global.json")
        .Executes(() =>
            DotNetTasks.DotNet(
                $"run --project \"{ToolsDirectory / "SdkVersionAnalyzer"}\" -- --verify \"{RootDirectory}\"",
                workingDirectory: RootDirectory));

    Target UpdateSdkVersions => d => d
        .Description("Rewrite pinned .NET SDK versions in workflows and dockerfiles (pass --housekeeping-sdk-version)")
        .Requires(() => SdkVersion)
        .Executes(() =>
            DotNetTasks.DotNet(
                $"run --project \"{ToolsDirectory / "SdkVersionAnalyzer"}\" -- --modify \"{RootDirectory}\" - - {SdkVersion}",
                workingDirectory: RootDirectory));

    Target GenerateDependencyList => d => d
        .Description("Regenerate docs/dependencies.md from the shippable packages' resolved dependency graphs")
        .Executes(() =>
        {
            var builder = new StringBuilder();
            builder.AppendLine("# Shipped package dependencies");
            builder.AppendLine();
            builder.AppendLine("<!-- Generated by `./eng/build.sh GenerateDependencyList`. Do not edit by hand. -->");

            foreach (var project in ShippablePackProjects)
            {
                Log.Information("Enumerating dependencies of {Project}", project.NameWithoutExtension);
                builder.AppendLine();
                builder.AppendLine(CultureInfo.InvariantCulture, $"## {project.NameWithoutExtension}");

                var frameworks = Generator.EnumerateDependencies(project)
                                 ?? throw new InvalidOperationException($"Dependency analysis failed for {project}.");

                foreach (var (framework, dependencies) in frameworks)
                {
                    builder.AppendLine();
                    builder.AppendLine(CultureInfo.InvariantCulture, $"### {framework}");
                    builder.AppendLine();
                    builder.AppendLine("| Package | Resolved version |");
                    builder.AppendLine("|---------|------------------|");
                    foreach (var dependency in dependencies)
                    {
                        builder.AppendLine(CultureInfo.InvariantCulture, $"| {dependency.Name} | {dependency.Version} |");
                    }
                }
            }

            DependencyListFile.Parent.CreateDirectory();
            File.WriteAllText(DependencyListFile, builder.ToString());
            Log.Information("Generated {File}", RootDirectory.GetRelativePathTo(DependencyListFile));
        });

    Target GenerateLibraryVersions => d => d
        .Unlisted()
        .Description("Generate the package version matrix sources into Artifacts/generated (opt-in scaffolding)")
        .Executes(() =>
            DotNetTasks.DotNet(
                $"run --project \"{ToolsDirectory / "LibraryVersionsGenerator"}\"",
                workingDirectory: RootDirectory));
}
