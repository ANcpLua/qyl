
using System.IO;
using Nuke.Common;
using Nuke.Common.IO;
using Nuke.Common.Tooling;
using Serilog;

namespace Qyl.Build;

[ParameterPrefix(nameof(IInstrument))]
interface IInstrument : INukeBuild
{
    const string JavaAgentVersion = "2.26.0";
    const string JavaAgentFileName = "opentelemetry-javaagent.jar";

    const string JavaAgentDownloadUrl =
        $"https://github.com/open-telemetry/opentelemetry-java-instrumentation/releases/download/v{JavaAgentVersion}/{JavaAgentFileName}";

    [Parameter("Target project directory (auto-detects language)")]
    string InstrumentPath => TryGetValue(() => InstrumentPath) ?? NukeBuild.RootDirectory;

    [Parameter("qyl collector OTLP endpoint")]
    string OtlpEndpoint => TryGetValue(() => OtlpEndpoint) ?? "http://localhost:4318";

    [Parameter("Service name for OTel resource")]
    string? ServiceName => TryGetValue(() => ServiceName);

    Target JavaAgent => d => d
        .Unlisted()
        .Description("Download OTel Java agent and generate qyl instrumentation config")
        .Executes(async () =>
        {
            AbsolutePath targetDir = InstrumentPath;
            var agentPath = targetDir / JavaAgentFileName;

            var isMaven = (targetDir / "pom.xml").FileExists();
            var isGradle = (targetDir / "build.gradle").FileExists() || (targetDir / "build.gradle.kts").FileExists();

            if (!isMaven && !isGradle)
                Log.Warning("No pom.xml or build.gradle found in {Path} — proceeding anyway", targetDir);

            var serviceName = ServiceName
                              ?? targetDir.Name;

            if (!agentPath.FileExists())
            {
                Log.Information("Downloading OTel Java Agent v{Version}...", JavaAgentVersion);
                ProcessTasks.StartProcess(
                        "curl", $"-L -o {agentPath} {JavaAgentDownloadUrl}",
                        targetDir)
                    .AssertZeroExitCode();
                Log.Information("Downloaded to {Path}", agentPath);
            }
            else
            {
                Log.Information("OTel Java Agent already exists at {Path}", agentPath);
            }

            var envFile = targetDir / "qyl-otel.env";
            await File.WriteAllTextAsync(envFile,
                $"""
                 # Generated by: nuke JavaAgent
                 # OTel Java Agent configuration for qyl
                 JAVA_TOOL_OPTIONS=-javaagent:{agentPath}
                 OTEL_SERVICE_NAME={serviceName}
                 OTEL_EXPORTER_OTLP_ENDPOINT={OtlpEndpoint}
                 OTEL_EXPORTER_OTLP_PROTOCOL=http/protobuf
                 OTEL_TRACES_EXPORTER=otlp
                 OTEL_METRICS_EXPORTER=otlp
                 OTEL_LOGS_EXPORTER=otlp
                 """);

            Log.Information("Generated {EnvFile}", envFile);

            var gitignore = targetDir / ".gitignore";
            if (gitignore.FileExists())
            {
                var content = await File.ReadAllTextAsync(gitignore);
                var addJar = !content.Contains(JavaAgentFileName);
                var addEnv = !content.Contains("qyl-otel.env");
                if (addJar || addEnv)
                {
                    var updated = content.TrimEnd() + "\n";
                    if (addJar) updated += $"{JavaAgentFileName}\n";
                    if (addEnv) updated += "qyl-otel.env\n";
                    await File.WriteAllTextAsync(gitignore, updated);
                    Log.Information(
                        "Updated .gitignore (jar={AddedJar}, env={AddedEnv})", addJar, addEnv);
                }
            }

            Log.Information("");
            Log.Information("╭─────────────────────────────────────────────────╮");
            Log.Information("│  qyl Java Agent ready                          │");
            Log.Information("╰─────────────────────────────────────────────────╯");
            Log.Information("");

            if (isMaven)
            {
                Log.Information("  Maven — add to .mvn/jvm.config:");
                Log.Information("    -javaagent:{Path}", agentPath);
                Log.Information("");
                Log.Information("  Or run with env file:");
                Log.Information("    source qyl-otel.env && mvn spring-boot:run");
            }
            else if (isGradle)
            {
                Log.Information("  Gradle — run with env file:");
                Log.Information("    source qyl-otel.env && ./gradlew bootRun");
            }
            else
            {
                Log.Information("  Run with env file:");
                Log.Information("    source qyl-otel.env && java -jar your-app.jar");
            }

            Log.Information("");
            Log.Information("  Docker Compose — add to service:");
            Log.Information("    env_file: [qyl-otel.env]");
            Log.Information("    volumes:");
            Log.Information("      - ./{HostPath}:/{ContainerPath}", JavaAgentFileName, JavaAgentFileName);
            Log.Information("");
        });
}
