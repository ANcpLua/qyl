class HealthRequestBuilder(BaseRequestBuilder):
    """
    Builds and executes requests for operations under /health
    """
    def __init__(self,request_adapter: RequestAdapter, path_parameters: Union[str, dict[str, Any]]) -> None:
        """
        Instantiates a new HealthRequestBuilder and sets the default values.
        param path_parameters: The raw url or the url-template parameters for the request.
        param request_adapter: The request adapter to use to execute the requests.
        Returns: None
        """
        super().__init__(request_adapter, "{+baseurl}/health", path_parameters)
    
    async def get(self,request_configuration: Optional[RequestConfiguration[QueryParameters]] = None) -> Optional[HealthResponse]:
        """
        Health check endpoint
        param request_configuration: Configuration for the request such as headers, query parameters, and middleware options.
        Returns: Optional[HealthResponse]
        """
        request_info = self.to_get_request_information(
            request_configuration
        )
        if not self.request_adapter:
            raise Exception("Http core is null") 
        from ..models.health_response import HealthResponse

        return await self.request_adapter.send_async(request_info, HealthResponse, None)
    
    def to_get_request_information(self,request_configuration: Optional[RequestConfiguration[QueryParameters]] = None) -> RequestInformation:
        """
        Health check endpoint
        param request_configuration: Configuration for the request such as headers, query parameters, and middleware options.
        Returns: RequestInformation
        """
        request_info = RequestInformation(Method.GET, self.url_template, self.path_parameters)
        request_info.configure(request_configuration)
        request_info.headers.try_add("Accept", "application/json")
        return request_info
    
    def with_url(self,raw_url: str) -> HealthRequestBuilder:
        """
        Returns a request builder with the provided arbitrary URL. Using this method means any other path or query parameters are ignored.
        param raw_url: The raw URL to use for the request builder.
        Returns: HealthRequestBuilder
        """
        if raw_url is None:
            raise TypeError("raw_url cannot be null.")
        return HealthRequestBuilder(self.request_adapter, raw_url)
    
    @property
    def live(self) -> LiveRequestBuilder:
        """
        The live property
        """
        from .live.live_request_builder import LiveRequestBuilder

        return LiveRequestBuilder(self.request_adapter, self.path_parameters)
    
    @property
    def ready(self) -> ReadyRequestBuilder:
        """
        The ready property
        """
        from .ready.ready_request_builder import ReadyRequestBuilder

        return ReadyRequestBuilder(self.request_adapter, self.path_parameters)
    
