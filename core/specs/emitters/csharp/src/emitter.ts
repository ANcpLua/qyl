import type { EmitContext, Enum, Model, Program } from "@typespec/compiler";
import { emitFile, navigateProgram } from "@typespec/compiler";
import { getCsharpNamespace, hasCsharpEnum, hasCsharpRecord } from "./decorators.js";
import { mapType } from "./type-map.js";

const HEADER = "﻿// <auto-generated/>\n// Copyright (c) 2025-2026 ancplua\n#nullable enable\n";

type Bucket = { namespace: string; models: Model[]; enums: Enum[] };

export async function $onEmit(context: EmitContext): Promise<void> {
  const program = context.program;
  if (program.compilerOptions.noEmit) return;

  const buckets = new Map<string, Bucket>();

  navigateProgram(program, {
    model: (m) => {
      if (m.namespace?.name === "TypeSpec") return;
      if (!m.name) return;
      const ns = resolveNamespace(program, m);
      if (!ns) return;
      getBucket(buckets, ns).models.push(m);
    },
    enum: (e) => {
      if (e.namespace?.name === "TypeSpec") return;
      const ns = resolveNamespace(program, e);
      if (!ns) return;
      getBucket(buckets, ns).enums.push(e);
    },
  });

  for (const [ns, bucket] of buckets) {
    const path = `${context.emitterOutputDir}/${ns.replace(/\./g, "/")}.cs`;
    await emitFile(program, { path, content: renderBucket(program, bucket) });
  }
}

function getBucket(buckets: Map<string, Bucket>, namespace: string): Bucket {
  let b = buckets.get(namespace);
  if (!b) {
    b = { namespace, models: [], enums: [] };
    buckets.set(namespace, b);
  }
  return b;
}

function resolveNamespace(program: Program, type: Model | Enum): string | undefined {
  const own = getCsharpNamespace(program, type);
  if (own) return own;
  let cursor = type.namespace;
  while (cursor) {
    const mapped = getCsharpNamespace(program, cursor);
    if (mapped) return mapped;
    cursor = cursor.namespace;
  }
  return undefined;
}

function renderBucket(program: Program, bucket: Bucket): string {
  const out: string[] = [HEADER];
  out.push(`namespace ${bucket.namespace};\n`);
  for (const m of bucket.models) out.push(renderModel(program, m));
  for (const e of bucket.enums) out.push(renderEnum(program, e));
  return out.join("\n");
}

function renderModel(program: Program, model: Model): string {
  const asRecord = hasCsharpRecord(program, model);
  const keyword = asRecord ? "public sealed record" : "public sealed class";
  const props: string[] = [];
  for (const [, prop] of model.properties) {
    const type = mapType(program, prop.type);
    const name = pascal(prop.name);
    if (prop.optional) {
      props.push(`    public ${type}? ${name} { get; init; }`);
    } else {
      props.push(`    public required ${type} ${name} { get; init; }`);
    }
  }
  const body = props.length ? `\n${props.join("\n")}\n` : "\n";
  return `${keyword} ${model.name}\n{${body}}\n`;
}

function renderEnum(program: Program, enm: Enum): string {
  const useEnumKeyword = hasCsharpEnum(program, enm);
  if (!useEnumKeyword) {
    // default: emit as string-const static class (friendlier to JSON)
    const members = [...enm.members.values()]
      .map((m) => `    public const string ${pascal(m.name)} = "${escapeLiteral(String(m.value ?? m.name))}";`)
      .join("\n");
    return `public static class ${enm.name}\n{\n${members}\n}\n`;
  }
  const body = [...enm.members.values()].map((m) => `    ${pascal(m.name)}`).join(",\n");
  return `public enum ${enm.name}\n{\n${body}\n}\n`;
}

function pascal(s: string): string {
  return s.length === 0 ? s : s[0].toUpperCase() + s.slice(1);
}

function escapeLiteral(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/"/g, "\\\"");
}
